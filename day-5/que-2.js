const key = 1;
const A = [0, 1, 2, 3, 4, 5];
const B = [5, 4, 3, 2, 1, 0];

//  index of the first occurrence of the key in array A
const indexA = A.indexOf(key);
console.log("Index of first occurrence of", key, "in array A:", indexA);

//  index of the first occurrence of the key in array B
const indexB = B.indexOf(key);
console.log("Index of first occurrence of", key, "in array B:", indexB);
