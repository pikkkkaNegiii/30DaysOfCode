// ================== sort an array =============
const A = [0, 1, 2, 3, 4, 5];
const B = [5, 4, 3, 2, 1, 0];

// Sort array A in ascending order
A.sort((a, b) => a - b);

// Sort array B in ascending order
B.sort((a, b) => a - b);

// Output the sorted arrays
console.log("Sorted A:", A);
console.log("Sorted B:", B);
